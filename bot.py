# Структура: {chat_id: [{"role": "user"/"model", "content": str, "time": datetime}, ...]}
# ВНИМАНИЕ: в Gemini API роль ассистента называется "model", а не "assistant"
message_history: Dict[int, List[dict]] = defaultdict(list)


def get_chat_id(message: Message) -> int:
    """
    Получаем уникальный ID чата.
    Для личных сообщений — это ID пользователя.
    Для групп — ID группы (чтобы история была общей для группы).
    """
    return message.chat.id


def clean_old_history(chat_id: int):
    """
    Очищаем сообщения старше HISTORY_TTL_MINUTES.
    Также ограничиваем количество сообщений до MAX_HISTORY_MESSAGES.
    """
    now = datetime.now()
    history = message_history[chat_id]

    # Удаляем устаревшие сообщения
    history[:] = [
        msg for msg in history
        if now - msg["time"] < timedelta(minutes=HISTORY_TTL_MINUTES)
    ]

    # Оставляем только последние MAX_HISTORY_MESSAGES сообщений
    if len(history) > MAX_HISTORY_MESSAGES:
        message_history[chat_id] = history[-MAX_HISTORY_MESSAGES:]


def build_contents_for_gemini(chat_id: int, new_user_text: str) -> List[genai_types.Content]:
    """
    Формируем список Content-объектов для отправки в Gemini API.

    В Gemini API используется другой формат сообщений, чем в OpenAI:
    - Каждое сообщение — это объект Content с полем role ("user" или "model")
    - Системный промпт передаётся через параметр system_instruction при создании запроса
    - История переписки передаётся как список Content
    """
    contents = []

    # Добавляем историю переписки
    for msg in message_history[chat_id]:
        contents.append(
            genai_types.Content(
                role=msg["role"],  # "user" или "model"
                parts=[genai_types.Part(text=msg["content"])]
            )
        )

    # Добавляем новое сообщение пользователя
    contents.append(
        genai_types.Content(
            role="user",
            parts=[genai_types.Part(text=new_user_text)]
        )
    )

    return contents


