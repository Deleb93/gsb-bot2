async def should_respond(message: Message) -> bool:
    """
    Определяем, должен ли бот ответить на сообщение.

    Условия ответа:
    1. Личное сообщение (private chat) — отвечаем ВСЕГДА
    2. Групповой чат — отвечаем ТОЛЬКО если:
       - Сообщение является ответом (reply) на сообщение бота
       - Или бота упомянули по @username
    """
    chat_type = message.chat.type

    # В личных сообщениях отвечаем всегда
    if chat_type == "private":
        return True

    # В группах проверяем упоминание или reply
    if chat_type in ("group", "supergroup"):
        # Проверяем, является ли сообщение ответом на сообщение бота
        if message.reply_to_message and message.reply_to_message.from_user:
            if message.reply_to_message.from_user.id == bot.id:
                return True

        # Проверяем, упомянут ли бот в тексте сообщения
        if message.text and f"@{bot._me.username}" in message.text:
            return True

        # Проверяем entities (упоминания через @username)
        if message.entities:
            for entity in message.entities:
                if entity.type == "mention":
                    mention = message.text[entity.offset:entity.offset + entity.length]
                    if mention == f"@{bot._me.username}":
                        return True

    return False


async def generate_ai_response(chat_id: int, user_text: str) -> str:
    """
    Генерируем ответ от ИИ через Google Gemini API.
    Используем историю сообщений для поддержания контекста диалога.

    ВНИМАНИЕ: Gemini API имеет свои особенности:
    - Системный промпт передаётся через system_instruction
    - Роль ассистента называется "model", а не "assistant"
    - История переписки — список Content-объектов
    """
    try:
        # Формируем историю сообщений для Gemini
        contents = build_contents_for_gemini(chat_id, user_text)

        # Настройка генерации (аналог temperature, max_tokens в OpenAI)
        config = genai_types.GenerateContentConfig(
            temperature=0.85,      # Немного креативности
            max_output_tokens=1500,  # Ограничение длины ответа
            top_p=1.0,
            top_k=40,
            system_instruction=SYSTEM_PROMPT  # Системный промпт передаётся здесь!
        )

        # Отправляем запрос к Gemini API
        response = gemini_client.models.generate_content(
            model=GEMINI_MODEL,
            contents=contents,
            config=config
        )

        # Извлекаем текст ответа
        ai_response = response.text.strip()

        return ai_response

    except Exception as e:
        logger.error(f"Ошибка при генерации ответа ИИ (Gemini): {e}")
        return (
            "⚠️ **КРИТИЧЕСКАЯ ОШИБКА СВЯЗИ** ⚠️\n\n"
            "*бьёт по кнопкам рации, хмурится*\n\n"
            "ГРАЖДАНИН! Связь с Департаментом Аналитики прервана!\n"
            "Возможные причины:\n"
            "• Перегрузка канала связи\n"
            "• Технические работы на сервере ГСБ\n"
            "• Исчерпан лимит бесплатного tier Gemini API\n"
            "• Попытка вмешательства аномалии №009 *шутка, конечно*\n\n"
            "Попробуйте повторить запрос через минуту. *отдаёт честь*"
        )


@dp.message(F.text)
async def handle_text_message(message: Message):
    """
    Главный обработчик текстовых сообщений.
    Определяет, нужно ли отвечать, и генерирует ответ через ИИ.
    """
    chat_id = get_chat_id(message)
    user_text = message.text
    user = message.from_user

    # Проверяем, должен ли бот ответить
    if not await should_respond(message):
        return

    # Логируем входящее сообщение
    logger.info(
        f"Сообщение от {user.id} (@{user.username}) в чате {chat_id}: {user_text[:50]}..."
    )

    # Очищаем старую историю
    clean_old_history(chat_id)

    # Показываем индикатор "печатает..."
    await bot.send_chat_action(chat_id=chat_id, action="typing")

    # Генерируем ответ от ИИ
    ai_response = await generate_ai_response(chat_id, user_text)

    # Сохраняем сообщение пользователя в историю
    message_history[chat_id].append({
        "role": "user",
        "content": user_text,
        "time": datetime.now()
    })

    # Сохраняем ответ бота в историю
    # ВНИМАНИЕ: в Gemini API роль ассистента — "model", а не "assistant"
    message_history[chat_id].append({
        "role": "model",
        "content": ai_response,
        "time": datetime.now()
    })

    # Отправляем ответ пользователю
    try:
        await message.answer(ai_response, parse_mode="Markdown")
    except Exception as e:
        # Если Markdown не сработал (например, из-за некорректного форматирования от ИИ),
        # отправляем как обычный текст
        logger.warning(f"Не удалось отправить с Markdown: {e}")
        await message.answer(ai_response)

    logger.info(f"Ответ отправлен в чат {chat_id}")


